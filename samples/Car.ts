class Car {
  brand: string
  color: string
  numberOfChairs: number
  numberOfDoors: number
  numberOfWheels: number

  constructor(brand: string, color: string) {
    this.brand = brand
    this.color = color
    this.numberOfChairs = 5
    this.numberOfDoors = 5
    this.numberOfWheels = 4
  }
}

